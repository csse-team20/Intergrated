    private void cmbChemicalsMouseMoved(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_cmbChemicalsMouseMoved
        
        String chem = cmbChemicals.getSelectedItem().toString();

        int filledRowCount = 0;
        rowSearch:
        for (int row = 0; row < tblChemical.getRowCount(); row++) {

            for (int col = 0; col < tblChemical.getColumnCount(); col++) {
                if (tblChemical.getValueAt(row, col) == null) {
                    break rowSearch;
                } else {
                    filledRowCount++;
                    continue rowSearch;
                }
            }
        }

        if (filledRowCount == 0 && !chem.equals("Select Chemical")) {
            tblChemical.setValueAt(chem, 0, 0);
        } else {
            rowSearch2:
            for (int row = 0; row < filledRowCount; row++) {
                if (!(tblChemical.getValueAt(row, 0)).equals(chem) && row == filledRowCount - 1) {
                    tblChemical.setValueAt(chem, filledRowCount, 0);
                    break rowSearch2;
                } else {
                    continue rowSearch2;
                }

            }
        }
    }//GEN-LAST:event_cmbChemicalsMouseMoved

