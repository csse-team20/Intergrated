    public void tableload() {

        try {
            //String sql1 = "SELECT l.testCode as 'Lab Test Code',l.testName as 'Lab Test Name',l.testType as 'Test Type',l.discription as 'Discription' FROM lab l";
            String sql1 = "SELECT l.testCode as 'Lab Test Code',l.testName as 'Lab Test Name',l.testType as 'Test Type',l.discription as 'Discription',group_concat(c.chemName) as 'Chemicals'"
                    + " FROM lab l,chemical c,labchem lc "
                    + "where l.testCode=lc.testCode and lc.chemCode=c.chemCode and l.statua = 'NT' "
                    + "group by l.testCode";
            p = c.prepareStatement(sql1);
            rs = p.executeQuery();
            jTable2.setModel(DbUtils.resultSetToTableModel(rs));

        } catch (SQLException ex) {
            System.out.println(ex);
        }
    }

