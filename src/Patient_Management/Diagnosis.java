    public void p_tableLoad() {

         try {
            String sql1 = "SELECT pid as 'Registration ID',fName as 'First Name',age as 'Age',catecory as 'Catecory' FROM patient";
            p = c.prepareStatement(sql1);
            rs = p.executeQuery();
            tblPData.setModel(DbUtils.resultSetToTableModel(rs));

        } catch (SQLException ex) {
            System.out.println(ex);
        }
    }
     
     public void d_tableLoad() {

         try {
            String sql1 = "SELECT s.dId as 'Diagnosis ID',s.disease as 'Diagnosis',s.ptpid as 'Patient ID',p.fname as 'Patient Name',p.age as 'Age',s.signs as 'Signs And Symptoms',s.general as 'General Findings',s.systemic as 'Systemic Findings' FROM diagnosis s , patient p WHERE p.pid=s.ptpid";
            p = c.prepareStatement(sql1);
            rs = p.executeQuery();
            tbldiag.setModel(DbUtils.resultSetToTableModel(rs));

        } catch (SQLException ex) {
            System.out.println(ex);
        }
    }
    
