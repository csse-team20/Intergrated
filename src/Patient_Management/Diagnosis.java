    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        int x = JOptionPane.showConfirmDialog(rootPane, "Do you want to Add this ?");

        if (x == 0) {
            
            diseaseId = lblDiag.getText();
            patientId = lblId.getText();
            pSigns = txtSigns.getText();
            pGeneral = txtGen.getText();
            pSystemic = txtSys.getText();
            pDiagnosis = txtDiag.getText();
            
            if(pSigns.isEmpty() || pGeneral.isEmpty() || pSystemic.isEmpty()) {
                JOptionPane.showMessageDialog(this, "You Should Fill All Fields");
            }
            else {
                Diagnosis_Data dSave = new Diagnosis_Data();
                dSave.save(diseaseId, patientId, pSigns, pGeneral, pSystemic, pDiagnosis);
                
                d_tableLoad();
                d_clearFields();            
         }  
        } 
    }//GEN-LAST:event_jButton2ActionPerformed

