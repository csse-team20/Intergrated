    private void lblLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_lblLoginActionPerformed
        

        
        LoginValidation loginVal = new LoginValidation();
        loginVal.checkEmpty(txtUserName, jpassword);
       
        String userName = txtUserName.getText();
        String password = String.valueOf(jpassword.getPassword());
        
        

        String query = "SELECT\n" +
                        "	login.`PASSWORD`,\n" +
                        "	login.USER_NAME,\n" +
                        "	login.NIC \n" +
                        "FROM\n" +
                        "	login \n" +
                        "WHERE\n" +
                        "	login.USER_NAME = '"+userName+"' \n" +
                        "	AND login.`PASSWORD` = '"+password+"'";
        Dbaccess da = new Dbaccess();
        try {
            ResultSet rs;
            String name = null ;
            String pass = null;
            
            rs = da.getData(query);

            while (rs.next()) {
                 name = rs.getString("USER_NAME");
                 pass = rs.getString("PASSWORD");
                 String nic = rs.getString("NIC");
                 
                if (userName.equals(name) && password.equals(pass)) {
                   
                    JOptionPane.showMessageDialog(jpassword,"Login Success");
                    dispose();
                    MainMenu menu = new MainMenu(userName,nic);
                    menu.setVisible(true);
                    this.setVisible(false);
                  //  UsersValidation userValidation = new UsersValidation();
                  //  userValidation.userPermission(nic);
                }
                
                
            }
            
              if(!userName.equals(name) || !password.equals(pass)){
                  JOptionPane.showMessageDialog(jpassword,"Login Failed");
                  
              }
              
              
              
              
              
              
              
        } catch (SQLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
 
    }//GEN-LAST:event_lblLoginActionPerformed

